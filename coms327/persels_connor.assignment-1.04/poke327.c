void print_rival_dist()
{
  int x, y;

  for (y = 0; y < MAP_Y; y++) {
    for (x = 0; x < MAP_X; x++) {
      if (world.rival_dist[y][x] == INT_MAX || world.rival_dist[y][x] < 0) {
        printf("   ");
      } else {
        printf(" %02d", world.rival_dist[y][x] % 100);
      }
    }
    printf("\n");
  }
}

void next_trainer_move(trainer_t *t) {
  int yMoves[] = {1, -1, 0, 1, -1,  0,  1, -1};
  int xMoves[] = {0,  0, 1, 1,  1, -1, -1, -1};
  int i;
  pair_t min;

  int cur_x = t->pos[dim_x];
  int cur_y = t->pos[dim_y];
  //printf("Unit location: (%d, %d) u: %d\n",cur_y,cur_x,t->type);
  switch(t->type) {
    case char_hiker:
      min[dim_x] = cur_x-1;
      min[dim_y] = cur_y-1;
      for (i=0; i<7; i++) {
        if (world.hiker_dist[cur_y+yMoves[i]][cur_x+xMoves[i]] < world.hiker_dist[min[dim_y]][min[dim_x]] && world.cur_map->character_map[min[dim_y]][min[dim_x]] != num_character_types) {
          min[dim_x] = cur_x+xMoves[i];
          min[dim_y] = cur_y+yMoves[i];
        }
      }

      //Couldn't find a valid spot, staying in place
      if (world.hiker_dist[min[dim_y]][min[dim_x]]==INT_MAX || world.cur_map->character_map[min[dim_y]][min[dim_x]] != num_character_types) {
        t->move_cost += 10;
        t->nextpos[dim_x] = t->pos[dim_x];
        t->nextpos[dim_y] = t->pos[dim_y];
      } else {
        t->move_cost += world.hiker_dist[min[dim_y]][min[dim_x]];
        t->nextpos[dim_x] = min[dim_x];
        t->nextpos[dim_y] = min[dim_y];
      }
      break;
    case char_rival:
      min[dim_x] = cur_x-1;
      min[dim_y] = cur_y-1;
      for (i=0; i<7; i++) {
        if (world.rival_dist[cur_y+yMoves[i]][cur_x+xMoves[i]] < world.rival_dist[min[dim_y]][min[dim_x]] && world.cur_map->character_map[min[dim_y]][min[dim_x]] != num_character_types) {
          min[dim_x] = cur_x+xMoves[i];
          min[dim_y] = cur_y+yMoves[i];
        }
      }

      //Couldn't find a valid spot, staying in place
      if (world.rival_dist[min[dim_y]][min[dim_x]]==INT_MAX || world.cur_map->character_map[min[dim_y]][min[dim_x]] != num_character_types) {
        t->move_cost += 10;
        t->nextpos[dim_x] = t->pos[dim_x];
        t->nextpos[dim_y] = t->pos[dim_y];
      } else {
        t->move_cost += world.rival_dist[min[dim_y]][min[dim_x]];
        t->nextpos[dim_x] = min[dim_x];
        t->nextpos[dim_y] = min[dim_y];
      }
      break;

    case char_pacer:
      //new trainer, placing vectors:
      if (t->move_cost==0) {
        int xx = rand() % 3;
        int yy = rand() % 3;
        t->vector_x = yMoves[xx];
        t->vector_y = yMoves[yy];
        if (t->vector_x == t->vector_y) {
          t->vector_y = (t->vector_y)*-1;
        }
      }

      if (world.rival_dist[cur_x+t->vector_y][cur_y+t->vector_y] != INT_MAX) {
        t->nextpos[dim_x] = cur_x+t->vector_x;
        t->nextpos[dim_y] = cur_y+t->vector_y;
        t->move_cost += world.rival_dist[t->nextpos[dim_y]][t->nextpos[dim_x]];
      } else {
        //flip vectors
        t->vector_x = t->vector_x*-1;
        t->vector_y = t->vector_y*-1;
        t->move_cost += 10;
      }

      break;
    
    case char_wanderer:
      if (t->move_cost==0) {
        int xx = rand() % 3;
        int yy = rand() % 3;
        t->vector_x = yMoves[xx];
        t->vector_y = yMoves[yy];
        if (t->vector_x == t->vector_y) {
          t->vector_y = (t->vector_y)*-1;
        }
      }
      
      if (world.rival_dist[cur_x+t->vector_y][cur_y+t->vector_x] != INT_MAX && world.cur_map->map[cur_y][cur_x] != world.cur_map->map[cur_y+t->vector_y][cur_x+t->vector_x]) {
        t->nextpos[dim_x] = cur_x+t->vector_x;
        t->nextpos[dim_y] = cur_y+t->vector_y;
        t->move_cost += world.rival_dist[t->nextpos[dim_y]][t->nextpos[dim_x]];
      } else {
        t->vector_x = -1;
        t->vector_y = -1;
        for (i=0; i<7; i++) {
          t->vector_x = xMoves[i];
          t->vector_y = yMoves[i];
          if (world.rival_dist[cur_x+t->vector_y][cur_y+t->vector_x] != INT_MAX && world.cur_map->map[cur_y][cur_x] != world.cur_map->map[cur_y+t->vector_y][cur_x+t->vector_x]) {
            t->nextpos[dim_x] = cur_x+t->vector_x;
            t->nextpos[dim_y] = cur_y+t->vector_y;
            t->move_cost += world.rival_dist[t->nextpos[dim_y]][t->nextpos[dim_x]];
            break;
          }
        }
      }
      break;
      
    case char_randomer:
      if (t->move_cost==0) {
        int xx = rand() % 3;
        int yy = rand() % 3;
        t->vector_x = yMoves[xx];
        t->vector_y = yMoves[yy];
        if (t->vector_x == t->vector_y) {
          t->vector_y = (t->vector_y)*-1;
        }
      }

      if (world.rival_dist[cur_x+t->vector_y][cur_y+t->vector_x] != INT_MAX) {
        t->nextpos[dim_x] = cur_x+t->vector_x;
        t->nextpos[dim_y] = cur_y+t->vector_y;
        t->move_cost += world.rival_dist[t->nextpos[dim_y]][t->nextpos[dim_x]];
      } else {
        t->vector_x = -1;
        t->vector_y = -1;
        for (i=0; i<7; i++) {
          t->vector_x = xMoves[i];
          t->vector_y = yMoves[i];
          if (world.rival_dist[cur_x+t->vector_y][cur_y+t->vector_x] != INT_MAX) {
            t->nextpos[dim_x] = cur_x+t->vector_x;
            t->nextpos[dim_y] = cur_y+t->vector_y;
            t->move_cost += world.rival_dist[t->nextpos[dim_y]][t->nextpos[dim_x]];
            break;
          }
        }
      }
      
      break;
    
    default:

      break;
  }
}

void rand_place_trainer(map_t *m, trainer_t *t) {
  int x=rand() % (MAP_X-1) + 1;
  int y=rand() % (MAP_Y-1) + 1;
  int size;

  switch (t->type) {
    case char_hiker:
      while (m->character_map[y][x]!=num_character_types && world.hiker_dist[y][x]!=INT_MAX) {
        x=rand() % (MAP_X-1) + 1;
        y=rand() % (MAP_Y-1) + 1;
      }
      break;

    default:
      while (m->character_map[y][x]!=num_character_types && world.rival_dist[y][x]!=INT_MAX) {
        x=rand() % (MAP_X-1) + 1;
        y=rand() % (MAP_Y-1) + 1;
      }
      break;
  }

  t->pos[dim_y] = y; t->pos[dim_x] = x;
  world.cur_map->character_map[y][x] = t->type;
  //allocating new space for trainers array...
  int ar1 = (sizeof (world.cur_map->trainers));
  int ar2 = (sizeof (world.cur_map->trainers[0]));
  size = ar1 / ar2;
  world.cur_map->trainers = realloc(world.cur_map->trainers,size + sizeof(trainer_t));
  world.cur_map->trainers[size] = *t;
  t->move_cost=0;

  //new movement location for next time + cost update
  next_trainer_move(t);
}

void add_trainers(map_t *m, int n) {
  int r;
  while (n > 0) {
// (rand() % (upper - lower + 1)) + lower;
    r = (rand() % (num_character_types - 2 )) + 1;
    trainer_t t;
    t.type = (character_type_t) r;

    rand_place_trainer(m,&t);
    n--;
  }
}

static int32_t trainer_cmp(const void *key, const void *with) {
  return (((trainer_t *) key)->move_cost - ((trainer_t *) with)->move_cost);
}

